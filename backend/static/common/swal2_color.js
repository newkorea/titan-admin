function swalColor(type){
    if(type == 'success'){
        return '#3fa556';
    }
    else if(type == 'error'){
        return '#3fa556';
    }
    else if(type == 'warning'){
        return '#facea8';
    }
    else if(type == 'info'){
        return '#3fc3ee';
    }
    else if(type == 'question'){
        return '#87adbd';
    }
    else{
        return '#333333';
    }
}